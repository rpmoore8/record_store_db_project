CREATE VIEW inventoryQuantity AS
	SELECT i.SKU, i.artist, i.title, i.genre, i.price, n.quantity, n.branchNo
	FROM Item i JOIN Inventory n
    ON i.SKU = n.SKU;
    
CREATE VIEW fullTransaction AS
	SELECT t.orderNo, t.branchNo, t.SKU, t.quantity, i.title, i.price, t.date_created
    FROM Transaction t JOIN Item i 
    ON t.SKU = i.SKU;
 


