CREATE TABLE Branch (
	branchNo INT NOT NULL AUTO_INCREMENT,
	phone VARCHAR(20),
    street VARCHAR(20),
    zip VARCHAR(7),
    city VARCHAR(20),
    state VARCHAR(2),
    PRIMARY KEY (branchNo)
);
    CREATE TABLE Staff (
	staffNo INT NOT NULL AUTO_INCREMENT,
	branchNo INT,
    username VARCHAR(30),
    password VARCHAR(40),
    firstName VARCHAR(20),
    lastName VARCHAR(20),
    adminRights INT,
    PRIMARY KEY (staffNo),
    FOREIGN KEY (branchNo) REFERENCES Branch(branchNo)
);

    CREATE TABLE Item (
	SKU INT NOT NULL,
    artist VARCHAR(60),
	title VARCHAR(80),
    genre VARCHAR(30),
    price DEC(5,2),
    PRIMARY KEY (SKU)
);

    
    CREATE TABLE Inventory (
	branchNo INT,
    SKU INT,
    quantity INT,
    PRIMARY KEY (branchNo, SKU),
    FOREIGN KEY (branchNo) REFERENCES Branch(branchNo),
	FOREIGN KEY (SKU) REFERENCES Item(SKU)
);

   
    CREATE TABLE Transaction (
	orderNo INT NOT NULL,
    branchNo INT NOT NULL,
    SKU INT,
    quantity INT,
    date_created DATETIME,
    PRIMARY KEY (orderNo, branchNo, SKU),
	FOREIGN KEY (branchNo) REFERENCES Branch(branchNo),
	FOREIGN KEY (SKU) REFERENCES Item(SKU)
);

