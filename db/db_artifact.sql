DELIMITER ;

DELIMITER $$
CREATE TRIGGER adjustInventory 
	AFTER INSERT ON Transaction 
	FOR EACH ROW
BEGIN
	UPDATE Inventory SET quantity=(quantity-new.quantity) WHERE SKU=new.SKU;
END;$$
DELIMITER ;


